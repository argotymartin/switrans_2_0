class Paquete {
  final int codigo;
  final String nombre;
  final String path;
  final bool isVisible;
  final bool isActivo;
  final String icono;
  final String fechaCreacion;

  Paquete({
    required this.codigo,
    required this.nombre,
    required this.path,
    required this.isVisible,
    required this.isActivo,
    required this.icono,
    required this.fechaCreacion,
  });
}
